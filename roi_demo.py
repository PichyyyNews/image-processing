# roi_demo.py
"""
ไฟล์ตัวอย่างการใช้งาน Region of Interest (ROI) และการหั่นภาพ (Array Slicing) ด้วย NumPy และ OpenCV
สำหรับสัปดาห์ที่ 2 (Week 2 Tutorial)
"""

import cv2
import numpy as np

def main():
    # =========================================================================
    # 1. ROI (Region of Interest) คืออะไร?
    # =========================================================================
    # ROI ย่อมาจาก "Region of Interest" แปลว่า "บริเวณที่เราสนใจในรูปภาพ"
    # ในการทำงานจริงกับรูปภาพ เราไม่จำเป็นต้องประมวลผลทั้งภาพเพื่อประหยัดทรัพยากรคอมพิวเตอร์
    # เช่น: ในภาพขนาดใหญ่ที่มีรถวิ่งอยู่ เราอาจจะตัด (Crop) เอาเฉพาะ "บริเวณป้ายทะเบียนรถ"
    # เพื่อส่งต่อให้โมเดล AI อ่านตัวหนังสือ ซึ่งบริเวณเล็ก ๆ นั้นแหละคือ ROI
    
    print("--- เริ่มต้นการสาธิตเรื่อง ROI ---")

    # สร้างภาพสีเหลี่ยมเปล่า ๆ ขึ้นมา 1 ภาพ ขนาด สูง 400 x กว้าง 600 พิกเซล (3 แชนเนลสี BGR)
    # เราใช้พื้นหลังเป็นสีเทาอ่อน [200, 200, 200] เพื่อให้เห็นสีอื่นชัดเจน
    img = np.ones((400, 600, 3), dtype=np.uint8) * 200

    # วาดรูปทรงอื่น ๆ เสมือนว่าเป็นวัตถุในรูปภาพจริง
    # วาดวงกลมสีส้มไว้ตรงกลางค่อนไปทางซ้าย
    cv2.circle(img, (200, 200), 80, (0, 100, 255), -1) 
    # วาดสี่เหลี่ยมสีน้ำเงินไว้ทางขวา
    cv2.rectangle(img, (400, 100), (550, 300), (255, 100, 0), -1)

    # =========================================================================
    # 2. การหั่นภาพดึง ROI: roi = img[y_start:y_end, x_start:x_end]
    # =========================================================================
    # กฎเหล็ก: NumPy จัดเก็บมิติภาพแบบ [y (แนวตั้ง/แถว), x (แนวนอน/คอลัมน์)]
    # ดังนั้นเวลาเราหั่นอาเรย์ (Slice) ต้องระบุพิกัดแกน y ก่อนแกน x เสมอ!
    #
    # สมมติเราสนใจ "บริเวณวงกลมสีส้ม" ซึ่งอยู่รอบ ๆ พิกัด:
    # - แกน Y (แนวตั้ง) ตั้งแต่ 100 ถึง 300 พิกเซล
    # - แกน X (แนวนอน) ตั้งแต่ 100 ถึง 300 พิกเซล
    y_start, y_end = 100, 300
    x_start, x_end = 100, 300

    # ดึงพื้นที่วงกลมสีส้มออกมาเป็นภาพใหม่ (นี่คือ ROI ของเรา)
    roi = img[y_start:y_end, x_start:x_end]

    # พิมพ์ตรวจสอบขนาดมิติภาพของ ROI
    # ขนาดภาพของ ROI จะมีส่วนสูง = y_end - y_start (200 พิกเซล) และ ความกว้าง = x_end - x_start (200 พิกเซล)
    print(f"มิติของภาพต้นฉบับ (Original Image Shape): {img.shape}")
    print(f"มิติของภาพ ROI (ROI Image Shape): {roi.shape}")

    # =========================================================================
    # 3. ข้อควรระวังระดับท็อป: View vs Copy
    # =========================================================================
    # การหั่นอาเรย์ใน NumPy แบบปกติ (`roi = img[...]`) จะเป็นการสร้าง "ภาพมุมมองเสมือน (View)"
    # ซึ่งไม่ได้เป็นการคัดลอกข้อมูลใหม่ในแรม หากเราแก้ไขค่าสีในภาพ `roi` 
    # ภาพต้นฉบับ `img` จะถูกแก้ไขไปด้วยโดยอัตโนมัติ!
    
    # ทดลองสร้างสำเนาของ ROI แยกออกไปเพื่อไม่ให้กระทบกับภาพหลัก (ใช้ .copy())
    roi_independent = img[y_start:y_end, x_start:x_end].copy()
    
    # ลองระบายสีทับลงไปบนภาพ roi (View) เป็นสีแดง [0, 0, 255] เฉพาะแถบบนซ้ายของพื้นที่ ROI
    # (พื้นที่นี้จะสะท้อนกลับไปเปลี่ยนสีบนภาพต้นฉบับ img ทันที)
    roi[0:50, 0:50] = [0, 0, 255] 
    
    # ลองระบายสีเขียว [0, 255, 0] บน roi_independent (Copy)
    # (พื้นที่นี้จะไม่ส่งผลใด ๆ ต่อภาพต้นฉบับ img)
    roi_independent[150:200, 150:200] = [0, 255, 0]

    # =========================================================================
    # 4. แสดงผลลัพธ์เพื่อเปรียบเทียบ
    # =========================================================================
    # แสดงภาพต้นฉบับ (สังเกตว่าจะมีจุดสี่เหลี่ยมสีแดงเกิดขึ้นตรงหัววงกลมสีส้มเนื่องจากเป็น View)
    cv2.imshow("Original Image (img)", img)
    
    # แสดงภาพ ROI ที่หั่นออกมา (จะเห็นพื้นที่วงกลมสีส้มที่ถูก Crop และมีหัวแดง)
    cv2.imshow("ROI extracted (roi - View)", roi)
    
    # แสดงภาพ ROI ที่คัดลอกแบบอิสระ (จะมีจุดสี่เหลี่ยมสีเขียวอยู่มุมล่างขวา แต่ไม่มีผลกระทบต่อภาพหลัก)
    cv2.imshow("Independent Copy ROI", roi_independent)

    print("\nกดปุ่มใด ๆ บนแป้นพิมพ์ขณะเลือกหน้าต่างภาพเพื่อปิดโปรแกรม...")
    cv2.waitKey(0)
    cv2.destroyAllWindows()
    print("ปิดหน้าต่างเรียบร้อยแล้ว!")

if __name__ == "__main__":
    main()
